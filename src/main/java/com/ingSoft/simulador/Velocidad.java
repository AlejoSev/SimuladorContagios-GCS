package com.ingSoft.simulador;

public class Velocidad {
	private int velx;
	private int vely;
	
	public Velocidad () {
		velx = 0;
		vely = 0;
	}
	public int getVelx() {
		return velx;
	}
	public void setVelx(int velx) {
		this.velx = velx;
	}
	public int getVely() {
		return vely;
	}
	public void setVely(int vely) {
		this.vely = vely;
	}

}
